//while loop 
//while loop executes till condition is true
{
    console.log("===================While loop====================");
    var num = 10;
    while (num >= 0) {
        console.log(num);
        num--;
    }
}


//Do While 
console.log("========================Do while loop===================");
var num=0;
do
{
    console.log(num);
    num++;
}while(num<=10)