//Other useful method in the string
//toUppercase and toLowercase
let sentence1="   Jai Shree Ram     ";
console.log(sentence1.toUpperCase());//   JAI SHREE RAM
console.log(sentence1.toLowerCase());//   jai shree ram

//Trim method is used to remove the white spaces in the string
//mostly used in the form to remove whitespaces from start and end
console.log(sentence1.trim());//Jai Shree Ram


